pn.section("Bounded Buffer Problem (Producer-Consumer)")
pn.definition(
    "<b>Bounded Buffer Problem:</b> A classic synchronization problem where a producer process "
    "puts items into a finite buffer and a consumer process removes them. "
    "Three semaphores are needed: "
    "<code>mutex</code> (binary, for mutual exclusion on buffer), "
    "<code>empty</code> (counting, # of empty slots, init=N), "
    "<code>full</code> (counting, # of filled slots, init=0)."
)
pn.code_block(
    """/* Bounded Buffer -- Semaphore Solution */

/* Shared variables */
int buffer[N];           /* circular buffer with N slots */
int in = 0, out = 0;    /* buffer pointers */

semaphore mutex = 1;     /* binary: mutual exclusion */
semaphore empty = N;     /* counting: # empty slots (start = N) */
semaphore full  = 0;     /* counting: # full slots  (start = 0) */

/* PRODUCER process */
do {
    /* produce an item in nextProduced */
    ...produce item...

    wait(empty);         /* block if no empty slot available */
    wait(mutex);         /* enter critical section */

    buffer[in] = nextProduced;
    in = (in + 1) % N;  /* advance circular pointer */

    signal(mutex);       /* leave critical section */
    signal(full);        /* notify consumer: one more full slot */

} while (true);

/* CONSUMER process */
do {
    wait(full);          /* block if no full slot available */
    wait(mutex);         /* enter critical section */

    nextConsumed = buffer[out];
    out = (out + 1) % N; /* advance circular pointer */

    signal(mutex);       /* leave critical section */
    signal(empty);       /* notify producer: one more empty slot */

    /* consume the item in nextConsumed */
    ...consume item...

} while (true);""",
    lang="c",
)
pn.tip(
    "IMPORTANT: Always wait(empty) BEFORE wait(mutex) in producer, and wait(full) BEFORE wait(mutex) "
    "in consumer. Reversing this order causes DEADLOCK."
)

