pn.section("Peterson's Solution for Critical Section")
pn.definition(
    "<b>Peterson's Solution:</b> A classical software-only solution to the critical section "
    "problem for TWO processes. It satisfies all three requirements: mutual exclusion, "
    "progress, and bounded waiting. Uses two shared variables: "
    "<code>turn</code> (whose turn to enter CS) and <code>flag[2]</code> (intent to enter CS)."
)
pn.code_block(
    """/* Peterson's Solution for TWO processes (i and j, where j = 1-i) */

int turn;       /* whose turn: 0 or 1 */
bool flag[2];   /* flag[i]=true means Pi wants to enter CS */

/* Process Pi's code: */
do {
    flag[i] = true;     /* Signal intent to enter CS */
    turn = j;           /* Give priority to the other process */

    /* BUSY WAIT: enter only when other doesn't want to enter OR it's our turn */
    while (flag[j] && turn == j)
        ;               /* wait */

    /* === CRITICAL SECTION === */

    flag[i] = false;    /* Done -- no longer interested */

    /* === REMAINDER SECTION === */

} while (true);

/* Why it works:
   - Mutual Exclusion: Both cannot pass while(flag[j] && turn==j) simultaneously
   - Progress: If Pj doesn't want CS (flag[j]=false), Pi enters immediately
   - Bounded Waiting: Pi waits at most ONE turn of Pj */""",
    lang="c",
)

